public class Operadores {
    public static void main(String [] args){
        int a,b,c;
        a = 70;
        b = 20;
        c = a + b;
        System.out.println("a + b = " + c);
        c = a - b;
        System.out.println("a - b = " + c);
        c = a * b;
        System.out.println("a * b = " + c);
        c = a / b;
        System.out.println("a / b = " + c);
        System.out.println("c++ = " + ++c);
        System.out.println();
        
    }
}
