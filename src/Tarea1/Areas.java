public class Areas {
    public double areaTriangulo(double base, double altura){
        double areaTotal = (base * altura)/2;
        return areaTotal;
    }
    public double areaRectangulo(double base, double altura){
        double areaTotal = (base * altura);
        return areaTotal;
    }
}
